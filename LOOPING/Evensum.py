#wrp to print sum of all even numbers from 1 to 100


start=1
sum=0
while  start <= 100:
     if start % 2 ==0:
          sum += start                                      
     start = start+1                  
print("The Sum Of All Even Numbers From 1 to 100 is :",sum)






